	/*		tomorrow(thisDate)												 */
	/*************************************************************************/
		function tomorrow(thisDate) {
			/** Change the date to tomorrow.  If today isn't the last day of the 
				month, add one to the day.  If it is, then determine if this month
				is not Decemeber, if it isn't Demember add one to the month and 
				reset day of month to zero.  Else, it is Decemeber and the year
				increases by one and the month and date are both reset to the 
				start.  If the year is reset, need to check if this new year is a 
				leap year, so run that funciton. **/
			// adjustForLeapYears(thisDate);
			if (thisDate[2]+1<=monthLength[thisDate[1]]) {
				return [thisDate[0],thisDate[1],thisDate[2]+1];
			} else if (thisDate[1]<11) {
				return [thisDate[0],thisDate[1]+1,1];
			} else {
				adjustForLeapYears('tomorrow()',thisDate[0]+1);
				return [thisDate[0]+1,0,1];
			}
		}	

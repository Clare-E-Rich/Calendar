	/*		startOfWeek()													 */
		/*	Determine what the date is for Sunday based on the targetDate	 */
		/*																	 */
		/*	If the targetDay is a Sunday, then the date of Sunday will be 	 */
		/*	the same as the targetDay's date. 								 */
		/*																	 */
		/*	If the targetDay isn't a Sunday, then we need to check if Sunday */
		/*	is in the same month as target day.  If so, then Sunday will be	 */
		/*	the targetDay-targetDayOfWeek.									 */
		/*																	 */
		/*	If Sunday isn't in the same month as targetDay, then we need to  */
		/*	go back a month to find the date of Sunday.  This will include a */
		/*	check if this is January or not.								 */
		/*																	 */
	/*************************************************************************/
		function startOfWeek() {
			// console.log('StarOfWeek :: targetYear 1 : ' + targetYear);
			// console.log('StarOfWeek :: targetDayOfWeek : '+targetDayOfWeek);
			if (targetDayOfWeek == 0) {
				targetSundayDate = [targetYear,targetMonth,targetDay];
				// console.log('if (targetDayOfWeek == 0) then '+targetSundayDate)
				// console.log('StarOfWeek :: targetYear 3 : ' + targetYear);
			} else if(targetDayOfWeek<=targetDay) {
				// console.log('if(targetDayOfWeek<=targetDay) :: ('+targetDayOfWeek+'<='+targetDay+')');
				targetSundayDate = [targetYear,targetMonth,
					targetDay-targetDayOfWeek];
				// console.log('StarOfWeek :: targetYear 4 : ' + targetYear);
			} else {
				// console.log('if(targetDayOfWeek>targetDay)  :: ('+targetDayOfWeek+'>'+targetDay+')');
				// console.log('targetMonth :: '+targetMonth);
				if (targetMonth==0) {
					var tempMonth = 11;
					var tempYear = targetYear-1;
				} else {
					var tempMonth = targetMonth - 1;
					var tempYear = targetYear;
				}
				// console.log('monthName[tempMonth] :: '+monthName[tempMonth]);
				var tempSundayDay = monthLength[tempMonth] 
					- (targetDayOfWeek-targetDay);
				targetSundayDate = [tempYear,tempMonth,tempSundayDay];
				// console.log('StarOfWeek :: targetYear 5 : ' + targetSundayDate[0]);
			}

			// console.log('StarOfWeek :: targetYear 2 : ' + targetYear);
			// console.log('function startOfWeek :: Sunday: '+ targetSundayDate[2]+' '+monthName[targetSundayDate[1]]+' '+targetSundayDate[0]);
			adjustForLeapYears('startOfWeek()',targetSundayDate[0]);
		}
